void	ft_sort_int_tab(int *tab, int size)
{
	int	s;
	int	i;

	i = 0;
	while (i < (size - 1))
	{
		if (tab[i] > tab[i + 1])
		{
			s = tab[i];
			tab[i] = tab[i + 1];
			tab[i + 1] = s;
		i = 0;
		}
		else
		{
		i++;
		}
	}
}
