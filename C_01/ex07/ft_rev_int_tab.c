void	ft_rev_int_tab(int *tab, int size)
{
	int	st;
	int	c;

	st = 0;
	c = 0;
	while (st < (size / 2))
	{
		c = tab[st];
		tab[st] = tab[size - 1 - st];
		tab[size - 1 - st] = c;
		st++;
	}
}
